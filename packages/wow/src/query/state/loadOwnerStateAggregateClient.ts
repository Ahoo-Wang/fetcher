import {
  api,
  ApiMetadata,
  ApiMetadataCapable,
  attribute,
  autoGeneratedError,
  get,
  path,
} from '@ahoo-wang/fetcher-decorator';

export class LoadOwnerStateAggregateEndpointPaths {
  static readonly LOAD = 'state';
  static readonly LOAD_VERSIONED = `${LoadOwnerStateAggregateEndpointPaths.LOAD}/{version}`;
  static readonly LOAD_TIME_BASED = `${LoadOwnerStateAggregateEndpointPaths.LOAD}/time/{createTime}`;
}

@api()
export class LoadOwnerStateAggregateClient<S> implements ApiMetadataCapable {
  constructor(public readonly apiMetadata?: ApiMetadata) {
  }

  @get(LoadOwnerStateAggregateEndpointPaths.LOAD)
  load(@attribute() attributes?: Record<string, any>, abortController?: AbortController): Promise<S> {
    throw autoGeneratedError(attributes, abortController);
  }

  @get(LoadOwnerStateAggregateEndpointPaths.LOAD_VERSIONED)
  loadVersioned(@path('version') version: number, @attribute() attributes?: Record<string, any>, abortController?: AbortController): Promise<S> {
    throw autoGeneratedError(version, attributes, abortController);
  }

  @get(LoadOwnerStateAggregateEndpointPaths.LOAD_TIME_BASED)
  loadTimeBased(@path('createTime') createTime: number, @attribute() attributes?: Record<string, any>, abortController?: AbortController): Promise<S> {
    throw autoGeneratedError(createTime, attributes, abortController);
  }

}